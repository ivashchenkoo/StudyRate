function openStat(evt, name, el) {
	var i, tabcontent, tablinks;

	if (el != document.getElementById("myque1")) {
		document.getElementById("myque1").classList.remove("current");
    }
	if (el != document.getElementById("myque2")) {
		document.getElementById("myque2").classList.remove("current");
	}
	if (el != document.getElementById("myque3")) {
		document.getElementById("myque3").classList.remove("current");
	}
	if (el != document.getElementById("myque4")) {
		document.getElementById("myque4").classList.remove("current");
	}
	
	$(el).addClass("current");

	tabcontent = document.getElementsByClassName("tabcontent");
	for (i = 0; i < tabcontent.length; i++) {
		tabcontent[i].style.display = "none";
	}

	tablinks = document.getElementsByClassName("tablinks");
	for (i = 0; i < tablinks.length; i++) {
		tablinks[i].className = tablinks[i].className.replace(" active", "");
	}

	document.getElementById(name).style.display = "block";
	evt.currentTarget.className += " active";
}

